.include <M328PDEF.inc>
	.dseg
	.org	0x100
count:	
	.byte 	2
	.equ	NN = 30
	.def	lpCnt = r19
	.def	cnt = r20
	.cseg
	.org	0x00
	ldi	ZL, low(array <<1)
	ldi	ZH, high (array << 1)
	ldi	cnt, 0
	ldi	lpCnt, NN
	loop:	lpm	r16, Z+
	andi	r16, 0x1C
	cpi	r16, 0x1C
	brne	next
	inc	cnt
next:	dec	lpCnt
	brne	loop
here:	rjmp	here
.org	0x200
